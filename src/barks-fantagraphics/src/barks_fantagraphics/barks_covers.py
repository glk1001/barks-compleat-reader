"""Carl Barks comic-book covers, from Michael Barrier's bibliography.

Bootstrap-generated by experiments/bibliography/parse_bibliography.py --emit-covers,
then hand-maintained (like comic_book_info.py). The reconciliation report verifies
the one-to-one match between these records and the bibliography's cover entries.
"""

from dataclasses import dataclass
from enum import Enum, auto

from .barks_titles import Titles
from .comic_issues import Issues


class CoverKind(Enum):
    FRONT = auto()
    INSIDE_FRONT = auto()
    INSIDE_BACK = auto()
    BACK = auto()


CoverKey = tuple[str, int, int, str, int]


@dataclass(frozen=True)
class BarksCover:
    issue_name: Issues | None  # None for series with no Issues member (cover-only albums)
    series_name: str  # bibliography H1 series name
    issue_number: int  # -1 for unnumbered issues
    issue_month: int
    issue_year: int
    kind: CoverKind
    seq: int  # 0-based tie-breaker within (series, issue, year, kind)
    qualifier: str | None  # e.g. "art only", "part", "pencil rough only"
    description: str
    submitted_day: int
    submitted_month: int
    submitted_year: int
    illustrates: Titles | None  # the curated story this cover illustrates, if any

    @property
    def key(self) -> CoverKey:
        return (self.series_name, self.issue_number, self.issue_year, self.kind.name, self.seq)


